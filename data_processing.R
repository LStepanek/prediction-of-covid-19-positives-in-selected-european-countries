## data (pre)processing -------------------------------------------------------

#### I am changing the variable types -----------------------------------------

for(
    my_variable in setdiff(
        
        colnames(my_data),
        c(
            "iso_code",
            "continent",
            "location",
            "date",
            "tests_units"
        )
        
    )
){
    
    my_data[, my_variable] <- as.numeric(
        
        my_data[, my_variable]
        
    )
    
}


#### I am changing a variable type of "date" variable to as.Date() ------------

my_data[, "date"] <- as.Date(
    
    my_data[, "date"],
    format = "%Y-%m-%d"
    
)


